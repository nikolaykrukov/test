package org.example;

public class Calc {

    public int summ(int a, int b) {
        int result;
        result = a + b;
        System.out.println(a + " + " + b + " = " + result);
        return result;
    }

    public int Minus(int a, int b) {
        int result;
        result = a - b;
        System.out.println(a + " - " + b + " = " + result);
        return result;
    }
}
